//   the three states kinetic equations

double ADJ_REACT_GEN(double *x, double *p)
{
	double z1, z2;
	
	z1=0.5*(sqrt(pow(p[0],2.)+2*p[0]*(p[1]-p[2]-p[3]-p[4]+p[5])+pow(p[1],2.)+2*p[1]*(p[2]-p[3]-p[4]-p[5])+pow(p[2],2.)+2*p[2]*(p[3]-p[4]-p[5])+pow(p[3],2.)+2*p[3]*(p[4]-p[5])+pow(p[4],2.)+2*p[4]*p[5]+pow(p[5],2.))+(p[0]+p[1]+p[2]+p[3]+p[4]+p[5]));

	z2=0.5*(sqrt(pow(p[0],2.)+2*p[0]*(p[1]-p[2]-p[3]-p[4]+p[5])+pow(p[1],2.)+2*p[1]*(p[2]-p[3]-p[4]-p[5])+pow(p[2],2.)+2*p[2]*(p[3]-p[4]-p[5])+pow(p[3],2.)+2*p[3]*(p[4]-p[5])+pow(p[4],2.)+2*p[4]*p[5]+pow(p[5],2.))-(p[0]+p[1]+p[2]+p[3]+p[4]+p[5]));
	
	return (p[5]*(p[1]+p[2])+p[0]*p[2]-p[5]*z1)/(z1*(z2+z1))*exp(-z1*(x[0]-p[6]))+(p[5]*(p[1]+p[2])+p[0]*p[2]+p[5]*z2)/(z2*(z2+z1))*exp(z2*(x[0]-p[6]))-(p[5]*(p[1]+p[2])+p[0]*p[2])/(z1*z2);
	
}

double ADJ_INT_GEN(double *x, double *p)
{
	double z1, z2;
	
	z1=0.5*(sqrt(pow(p[0],2.)+2*p[0]*(p[1]-p[2]-p[3]-p[4]+p[5])+pow(p[1],2.)+2*p[1]*(p[2]-p[3]-p[4]-p[5])+pow(p[2],2.)+2*p[2]*(p[3]-p[4]-p[5])+pow(p[3],2.)+2*p[3]*(p[4]-p[5])+pow(p[4],2.)+2*p[4]*p[5]+pow(p[5],2.))+(p[0]+p[1]+p[2]+p[3]+p[4]+p[5]));

	z2=0.5*(sqrt(pow(p[0],2.)+2*p[0]*(p[1]-p[2]-p[3]-p[4]+p[5])+pow(p[1],2.)+2*p[1]*(p[2]-p[3]-p[4]-p[5])+pow(p[2],2.)+2*p[2]*(p[3]-p[4]-p[5])+pow(p[3],2.)+2*p[3]*(p[4]-p[5])+pow(p[4],2.)+2*p[4]*p[5]+pow(p[5],2.))-(p[0]+p[1]+p[2]+p[3]+p[4]+p[5]));
	
	return (p[0]*(p[3]+p[4])+p[3]*p[5]-p[0]*z1)/(z1*(z2+z1))*exp(-z1*(x[0]-p[6]))+(p[0]*(p[3]+p[4])+p[3]*p[5]+p[0]*z2)/(z2*(z2+z1))*exp(z2*(x[0]-p[6]))-(p[0]*(p[3]+p[4])+p[3]*p[5])/(z1*z2);
	
}

double ADJ_NO_REACT_GEN(double *x, double *p)
{
	double z1, z2;
	
	z1=0.5*(sqrt(pow(p[0],2.)+2*p[0]*(p[1]-p[2]-p[3]-p[4]+p[5])+pow(p[1],2.)+2*p[1]*(p[2]-p[3]-p[4]-p[5])+pow(p[2],2.)+2*p[2]*(p[3]-p[4]-p[5])+pow(p[3],2.)+2*p[3]*(p[4]-p[5])+pow(p[4],2.)+2*p[4]*p[5]+pow(p[5],2.))+(p[0]+p[1]+p[2]+p[3]+p[4]+p[5]));

	z2=0.5*(sqrt(pow(p[0],2.)+2*p[0]*(p[1]-p[2]-p[3]-p[4]+p[5])+pow(p[1],2.)+2*p[1]*(p[2]-p[3]-p[4]-p[5])+pow(p[2],2.)+2*p[2]*(p[3]-p[4]-p[5])+pow(p[3],2.)+2*p[3]*(p[4]-p[5])+pow(p[4],2.)+2*p[4]*p[5]+pow(p[5],2.))-(p[0]+p[1]+p[2]+p[3]+p[4]+p[5]));
	
	return (pow(z1,2.)-z1*(p[1]+p[2]+p[3]+p[4])+p[1]*p[3]+p[1]*p[4]+p[2]*p[4])/(z1*(z2+z1))*exp(-z1*(x[0]-p[6]))+(pow(z2,2.)+z2*(p[1]+p[2]+p[3]+p[4])+p[1]*p[3]+p[1]*p[4]+p[2]*p[4])/(z2*(z2+z1))*exp(z2*(x[0]-p[6]))-(p[1]*p[3]+p[1]*p[4]+p[2]*p[4])/(z1*z2);
	
}
